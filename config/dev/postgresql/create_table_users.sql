	UUID uuid PRIMARY KEY,
	FIRST_NAME varchar (100) NOT NULL,
	LAST_NAME varchar (100) NOT NULL,
    SLACK varchar (100) NOT NULL,
    SKYPE varchar (100) NOT NULL,
	LASTUPDATED bigint
);

